void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {

	HAL_UART_Receive_IT(&huart2, (uint8_t*) rxdata, 16);

	char *str;
	char *p2;

	char buffer[32];

	str = strstr(rxdata, "$");

	if (str != NULL) {
		p2 = strstr(str, "\n");
		if (p2 != NULL) {
			memset(buffer, 0, 32);
			sprintf(buffer, "%.*s", p2 - str - 1, str + 1);
			qtime = atoi(buffer);
		}
	}
}

